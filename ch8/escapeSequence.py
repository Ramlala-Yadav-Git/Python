
print("Ramlala\a yadav")  # \a  Bell
print("Ramlala\b yadav")  # \b  Backspace
print("Ramlala\f yadav")  # \f  Formfeed
print("Ramlala\n yadav")  # \n  NewLine
print("Ramlala\r yadav")  # \r  Carriage Return
print("Ramlala\t yadav")  # \t  Horizontal Tab
print("Ramlala\v yadav")  # \v  Vertical Tab
print("Ramlala\newline yadav")  # \newline  Backlash and NewLine ignored
